// Helper to get font by ID
export function getFontById(id: string): FontFamily | undefined {
  return fonts.find((f) => f.id === id);
}

// Helper to get fonts by classification
export function getFontsByClassification(classification: FontFamily["classification"]): FontFamily[] {
  return fonts.filter((f) => f.classification === classification);
}

// Helper to get fonts by source
export function getFontsBySource(source: FontFamily["source"]): FontFamily[] {
  return fonts.filter((f) => f.source === source);
}
